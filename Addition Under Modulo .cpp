class Solution{
    public:
        long long sumUnderModulo(long long a,long long b)
    {
        int m=1000000007;
        a=a%m;
        b=b%m;
        return (a+b)%m;
    }
};

// { Driver Code Starts.
int main() {
	int T;
	cin>>T;
	while(T--)
	{
	    long long a;
	    long long b;
	    cin>>a>>b;
	    Solution ob;
	    cout<<ob.sumUnderModulo(a,b)<<endl;
	}
	return 0;
}  // } Driver Code Ends
